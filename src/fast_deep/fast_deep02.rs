/*

시행(trial):반복할수 있으며 매변 결과가 달라질수 있는 실험
사건(event):시행에 따른 결과를 의미

확률 어떠한 event가 일어날 가능성을 수로 표현한것

확률변수:사건으로 인해 그 그 값이 확률적으로 정해지는 변수
주사위 2개를 던지는 시행을할떄마다 눈금의 합이 변할수 있다.
확률변수= 눈금의합
확률 변수는 대문자 X,확률변수가 취할수 있는 값은 소문자 x

확률 함수란 확률변수에 따라서 확률값을 부여하는 함수
주사위 2개를 던져서 눈금의합이 3이 나올확률은 2/36
눈금이 합이 3이 되는 경우 (1,2)(2,1)두가지 경우 존재
P(x=3)=2/36


2개의 동전을 던지는 시행에 대해서 각 사건의 확률은
눈금의 합이 0인사건 이 발생할 확률 P(X = 0)= 1/4
눈금의 합이 1인사건 이 발생할 확률 P(X = 1)= 1/2
눈금의 합이 2인사건 이 발생할 확률 P(X = 2)= 1/4
 */